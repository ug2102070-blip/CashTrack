@freezed
@HiveType(typeId: 3)
class CategoryModel with _$CategoryModel {
  const factory CategoryModel({
    @HiveField(0) required String id,
    @HiveField(1) required String name,
    @HiveField(2) required String nameBn,
    @HiveField(3) required String icon,
    @HiveField(4) required String colorHex,
    @HiveField(5) required CategoryType type,
    @HiveField(6) @Default(false) bool isDefault,
    @HiveField(7) @Default(false) bool isDeleted,
    @HiveField(8) DateTime? createdAt,
  }) = _CategoryModel;

  factory CategoryModel.fromJson(Map<String, dynamic> json) =>
      _$CategoryModelFromJson(json);
}

@HiveType(typeId: 4)
enum CategoryType {
  @HiveField(0)
  expense,
  @HiveField(1)
  income,
  @HiveField(2)
  both,
}

